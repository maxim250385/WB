def detect_category(raw_text: str, gemini_category: str | None = None) -> str:
    """
    1. Если Gemini вернула категорию → маппим на наши ключи
    2. Keyword-поиск только по первым CATEGORY_DETECT_CHARS символам
       (название товара всегда в начале, большие PDF не путают категорию)
    3. DEFAULT_CATEGORY
    """
    # 1. Ответ Gemini
    if gemini_category:
        matched = _match_to_known(gemini_category.lower().strip())
        if matched:
            return matched

    # 2. Keyword-поиск по началу текста
    # Берём первые N символов — название и ключевые характеристики всегда в начале
    text_head  = raw_text[:CATEGORY_DETECT_CHARS].lower()

    # Строгий проход: минимум 2 совпадения
    for category, keywords in _KEYWORDS.items():
        hits = sum(1 for kw in keywords if kw in text_head)
        if hits >= 2:
            return category

    # Мягкий проход: одно совпадение (для коротких текстов)
    for category, keywords in _KEYWORDS.items():
        if any(kw in text_head for kw in keywords):
            return category

    # 3. Fallback — ищем в полном тексте (только мягкий проход)
    text_lower = raw_text.lower()
    for category, keywords in _KEYWORDS.items():
        if any(kw in text_lower for kw in keywords):
            return category

    return DEFAULT_CATEGORY


def _match_to_known(text: str) -> str | None:
    known = list(CATEGORY_EXTRA_FIELDS.keys())
    if text in known:
        return text
    if text in _GEMINI_ALIASES:
        return _GEMINI_ALIASES[text]
    for k in known:
        if k in text or text in k:
            return k
    for alias, target in _GEMINI_ALIASES.items():
        if alias in text or text in alias:
            return target
    return None


def build_fields_for_category(category: str) -> list[str]:
    extra  = CATEGORY_EXTRA_FIELDS.get(category, [])
    seen   = set()
    result = []
    for f in BASE_FIELDS + extra:
        if f not in seen:
            seen.add(f)
            result.append(f)
    return result
